void render_primary_oled(void) {
    // Layer info
    oled_write_P(PSTR("Layer: "), false);
    switch (get_highest_layer(layer_state)) {
        case 0:
            oled_write_P(PSTR("QWERTY\n"), false);
            break;
        case 1:
            oled_write_P(PSTR("LOWER\n"), false);
            break;
        case 2:
            oled_write_P(PSTR("RAISE\n"), false);
            break;
        default:
            oled_write_P(PSTR("UNKNOWN\n"), false);
    }
    
    // WPM counter
    oled_write_P(PSTR("WPM: "), false);
    oled_write(get_u8_str(get_current_wpm(), ' '), false);
    oled_write_P(PSTR("\n"), false);
    
    // Active modifiers
    uint8_t modifiers = get_mods() | get_oneshot_mods();
    oled_write_P(PSTR("Mods: "), false);
    oled_write_P((modifiers & MOD_MASK_SHIFT) ? PSTR("SHF ") : PSTR("    "), false);
    oled_write_P((modifiers & MOD_MASK_CTRL)  ? PSTR("CTL ") : PSTR("    "), false);
    oled_write_P((modifiers & MOD_MASK_ALT)   ? PSTR("ALT ") : PSTR("    "), false);
    oled_write_P((modifiers & MOD_MASK_GUI)   ? PSTR("GUI ") : PSTR("    "), false);
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        // Primary display (left side)
        render_primary_oled();
    } else {
        // Secondary display (right side) - your photo
        oled_write_raw_P(my_photo, sizeof(my_photo));
    }
    return false;
}